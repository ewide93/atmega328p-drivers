//==================================================================================================
// Include directives.
//==================================================================================================
#include "delay.h"


//==================================================================================================
// Function definitions.
//==================================================================================================
void BlockingDelay_ms(U16 Delay_ms)
{
    for (U16 i = 0; i < Delay_ms; i++)
    {
        _delay_ms(1);
    }
    return;
}

void BlockingDelay_us(U16 Delay_us)
{
    for (U16 i = 0; i < Delay_us; i++)
    {
        _delay_us(1);
    }
    return;
}

