static inline void UART_TxEnable(void)
{
    UCSR0B |= (1 << TXEN0);
}

static inline void UART_TxDisable(void)
{
    UCSR0B &= ~(1 << TXEN0);
}

static inline void UART_RxEnable(void)
{
    UCSR0B |= (1 << RXEN0);
}

static inline void UART_RxDisable(void)
{
    UCSR0B &= ~(1 << RXEN0);
}


